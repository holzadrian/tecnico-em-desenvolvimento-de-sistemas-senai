# Ele cria uma listinha com alguns preços
precos = [49.53, 120.20, 28.75, 64.30, 81.96]

# Usa max() e min() para encontrar os maiores e menores precos
maior_preco = max(precos)
menor_preco = min(precos)

# Mostra os resultados
print(f"Maior preço: {maior_preco}")
print(f"Menor preço: {menor_preco}")